print("Variables :-")
name = "Mohajith Pundarik"
age = 20
college = "Venkata Rama Degree college"

print(name)
print(age)
print(college)
#output will be "Mohajith Pundarik", 20 and "Venkata Rama Degree college"

print("Data types :- Integer")
a = 10
b = 90
print("the sum of two numbers is ", a + b)
#output will be 100

print("Data types- float :-")
f1 = 10.8
f2 = 90.9
print("Value of f1 is :", f1)
print("Value of f2 is :", f2)
#output will be 10.8 and 90.9

print("Data types- string :-")
s1 = "Mohajith"
s2 = "Bsc Computer Science"
print("My name is ", s1)
print("I am studying ", s2)
#output will be "My name is Mohajith" and "I am studying Bsc Computer Science"  

print("Data types- boolean :-")
is_student = True
is_employed = False
print("Is the person a student?", is_student)
print("Is the person employed?", is_employed)
#output will be "Is the person a student? True" and "Is the person employed
# False"

print("Collection data types- list :-")
Games = ["FreeFire", "Pubg", "Call of Duty"]
print("My favorite games are:", Games)
#output will be "My favorite games are: ['FreeFire', 'Pubg', 'Call of Duty']"

print("Collection data types- tuple :-")
Movies = ("Tumbaad" , "Kantara" , "Pushpa")
print("My favorite movies are:", Movies)
#output will be "My favorite movies are: ('Tumbaad', 'Kantara', 'Pushpa')"

print("Collection data types- set :-")
Fruits = {"Apple", "Banana", "Orange"}          
print("My favorite fruits are:", Fruits)
#output will be "My favorite fruits are: {'Apple', 'Banana', 'Orange'}"

print("Collection data types- dictionary :-")
person = {
    "name": "Mohajith Pundarik",
    "age": 20,
    "college": "Venkata Rama Degree college"
}
print("Person details are:", person)    
#output will be "Person details are: {'name': 'Mohajith Pundarik', 'age': 20, 'college': 'Venkata Rama Degree college'}"

print("Special data types- None :-")
value = None
print("The value is:", value)
#output will be "The value is: None"

